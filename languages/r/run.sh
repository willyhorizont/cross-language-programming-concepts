#!/bin/bash

source "$(dirname "$(realpath "$0")")/../../tools/runner.sh" "$0" "$@"

CPV="
echo \">docker images\"
echo \"$IMG\"
echo \">R --version\"
R --version
"

CRLC="
cd \"$PTFNXD\"
Rscript \"$FNX\"
"

